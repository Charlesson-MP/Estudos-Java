/*Exemplo Dialog1.java
 *Usa a classe JOptionPane para exibir Welcome to Java em uma caixa de dialogo.
 */

import javax.swing.JOptionPane; //Importando a classe JOptionPane da Java API.

public class Dialog1 { //Início da classe Dialog1.
    
    //Método main, inicia a execução da aplicação
    public static void main(String[] args) { //Início do método main.
        
        JOptionPane.showMessageDialog(null,
    "Welcome to Java"); //Exibe a caixa com a mensagem específicada nos parâmetros

    } //Fim do método main.

} //Fim da classe Dialog1.